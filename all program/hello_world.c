#include <stdio.h>      //header file

int main() {
    printf("Hello, World!\n");      //display
    printf("Hello, \bWorld!\n");    //backspace
    printf("Hello, World!\n");      //next line
    printf("\"Hello,World!\"\n");   //double quote
    printf("Hello,\\ World!\n");    //insert "\\"
    printf("Hello,\t World!\n");    //insert tab
    return 0;
}